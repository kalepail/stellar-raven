#!/usr/bin/env node
/**
 * build-op-classes.mjs — generate eval/plan/op-classes.json from the catalog.
 *
 * Classifies every `kind: "operation"` entry in catalog/manifest.json as:
 *   "broad"  — returns a collection (search/list/find/match…): the entry point
 *              of a broad→detail progression
 *   "detail" — returns one thing by id/name/slug (get/read/explain…): the
 *              follow-up half of a progression
 *   "meta"   — service self-description or non-reading actions (status,
 *              changelog, schemas/vocabularies, writes, metered compute) —
 *              neither half of a progression
 *
 * Classification is data, not prose: a prefix→class map (over the snake_cased
 * first token of the op name, so scout's camelCase `searchProjects` matches
 * "search") plus explicit per-op overrides for ops whose prefix lies about the
 * return shape (plural `get*` collection endpoints, vocab/enum `get_*`
 * discovery, write/compute POSTs). Anything neither map covers falls to "meta"
 * AND is listed under `unmatched` + warned at build time, so misclassification
 * stays visible instead of silent.
 *
 * Output is deterministic (sorted keys, no timestamp) — rebuild with:
 *   node eval/plan/build-op-classes.mjs
 * Never hand-edit op-classes.json (CLAUDE.md: generated artifacts are rebuilt
 * by scripts).
 */
import { readFileSync } from "node:fs";
import { fileURLToPath, pathToFileURL } from "node:url";
import path from "node:path";
import { writeFileAtomic } from "../../scripts/lib/shared.mjs";

const PLAN_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(PLAN_DIR, "..", "..");
const MANIFEST_PATH = path.join(ROOT, "catalog", "manifest.json");
const OUT_PATH = path.join(PLAN_DIR, "op-classes.json");

/** First-token prefix → class (token taken after camelCase→snake_case). */
const PREFIX_CLASSES = {
  search: "broad",
  list: "broad",
  find: "broad",
  match: "broad",
  get: "detail",
  read: "detail",
  explain: "detail",
  compare: "detail", // reads specific named things (slugs) side-by-side
  status: "meta",
  changelog: "meta",
  openapi: "meta",
  help: "meta"
};

/**
 * Per-op overrides where the prefix misleads. Grouped by reason:
 * - plural/collection `get*` endpoints return row arrays, not one thing
 *   (research/services/stellar-light.md endpoint inventory: hackathons[],
 *   partners[], rfps[], builders[], clusters[], leaderboard projects[];
 *   lumenloop get_related_projects returns a related-project collection)
 * - analytics rollups are ecosystem-wide entry points, i.e. broad
 * - vocab/enum/schema discovery and status/changelog are meta
 * - AI compute that is not a read progression step (partnerOnboard) is meta
 *
 * Overrides are keyed on EXPOSED catalog ops only — classifyOp is only ever
 * called for ops in the manifest, so keys for build-time-excluded ops
 * (writes, the paid research lane, ADR-0003) would be dead data.
 */
const OP_OVERRIDES = {
  // collections behind singular-looking prefixes → broad
  "scout.getHackathons": "broad",
  "scout.getPartners": "broad",
  "scout.getRfps": "broad",
  "scout.getBuilders": "broad",
  "scout.getClusters": "broad",
  "scout.getLeaderboard": "broad",
  "scout.analyzeEcosystem": "broad",
  "lumenloop.get_related_projects": "broad",
  // vocab/enum/schema discovery → meta
  "scout.getStatus": "meta",
  "scout.getChangelog": "meta",
  "lumenloop.get_categories": "meta",
  "lumenloop.get_regions": "meta",
  "lumenloop.get_tags_vocabulary": "meta",
  "lumenloop.get_project_tags_vocabulary": "meta",
  // AI compute, not a read progression step → meta
  "scout.partnerOnboard": "meta"
};

/** camelCase → snake_case, then the first token: "searchProjects" → "search". */
export function firstToken(opName) {
  return opName
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .toLowerCase()
    .split("_")[0];
}

/** Classify one full op id ("service.op_name") → { class, matched }. */
export function classifyOp(opId) {
  if (opId in OP_OVERRIDES) return { cls: OP_OVERRIDES[opId], matched: true };
  const opName = opId.slice(opId.indexOf(".") + 1);
  const cls = PREFIX_CLASSES[firstToken(opName)];
  if (cls) return { cls, matched: true };
  return { cls: "meta", matched: false };
}

export function buildOpClasses(manifest) {
  const classes = {};
  const unmatched = [];
  const ids = manifest.entries
    .filter((e) => e.kind === "operation")
    .map((e) => e.id)
    .sort();
  for (const id of ids) {
    const { cls, matched } = classifyOp(id);
    classes[id] = cls;
    if (!matched) unmatched.push(id);
  }
  return {
    $comment:
      "GENERATED by eval/plan/build-op-classes.mjs from catalog/manifest.json — never hand-edit. broad = returns collections; detail = one thing by id/name; meta = self-description, vocab, writes, metered compute.",
    source: "catalog/manifest.json",
    manifestVersion: manifest.version,
    classes,
    unmatched
  };
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  const manifest = JSON.parse(readFileSync(MANIFEST_PATH, "utf8"));
  const out = buildOpClasses(manifest);
  writeFileAtomic(OUT_PATH, JSON.stringify(out, null, 2) + "\n");
  const counts = { broad: 0, detail: 0, meta: 0 };
  for (const c of Object.values(out.classes)) counts[c]++;
  console.log(
    `wrote ${OUT_PATH}: ${Object.keys(out.classes).length} ops (${counts.broad} broad, ${counts.detail} detail, ${counts.meta} meta)`
  );
  if (out.unmatched.length) {
    console.warn(
      `WARNING: ${out.unmatched.length} op(s) matched no prefix and no override — defaulted to "meta"; ` +
        `verify and extend PREFIX_CLASSES/OP_OVERRIDES:\n  ${out.unmatched.join("\n  ")}`
    );
  }
}
