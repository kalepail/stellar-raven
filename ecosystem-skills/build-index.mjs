#!/usr/bin/env node
//
// build-index.mjs — regenerate INDEX.md from MANIFEST.json + groups.json.
//
// For each synced skill it reads the local SKILL.md and extracts the skill
// `name` + `description` straight from its YAML frontmatter, so the index stays
// fresh without hand-maintained copy. Skills are grouped by theme (groups.json);
// any skill present in the manifest but not filed in a group lands in an
// "Uncategorized" section so newly synced skills are impossible to miss. The
// stellarlight.xyz ecosystem DIRECTORY (manifest.catalog) is rendered as a
// separate "what exists in the ecosystem" map (incl. non-skill-md tools/SDKs).
//
// Run automatically by update.sh; safe to run standalone after a sync.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const DIR = dirname(fileURLToPath(import.meta.url));
const SKILLS = join(DIR, "skills");

const manifest = JSON.parse(readFileSync(join(DIR, "MANIFEST.json"), "utf8"));
const { groups } = JSON.parse(readFileSync(join(DIR, "groups.json"), "utf8"));

// Build an index of every synced skill: id "source/skill" -> metadata.
const skillById = new Map();
const sourceById = new Map();
for (const src of manifest.sources) {
  sourceById.set(src.id, src);
  for (const skill of src.skills) {
    const id = `${src.id}/${skill.name}`;
    const totalSize = skill.files.reduce((n, f) => n + f.size, 0);
    skillById.set(id, { id, source: src.id, name: skill.name, files: skill.files, totalSize });
  }
}

/** Extract `name` + `description` from a SKILL.md YAML frontmatter block. */
function frontmatter(relSkillDir, skillName) {
  // Prefer a SKILL.md; fall back to the first .md file.
  const skill = skillById.get(`${relSkillDir}`);
  let mdPath;
  if (skill) {
    const f = skill.files.find((x) => x.path === "SKILL.md") || skill.files[0];
    mdPath = f && join(SKILLS, skill.source, skillName, f.path);
  }
  if (!mdPath) return { name: skillName, description: "" };
  let text;
  try {
    text = readFileSync(mdPath, "utf8");
  } catch {
    return { name: skillName, description: "" };
  }
  const m = text.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!m) return { name: skillName, description: "" };
  const body = m[1];
  const field = (key) => {
    const re = new RegExp(`^${key}:\\s*([\\s\\S]*?)(?=\\n[A-Za-z0-9_-]+:|$)`, "m");
    const hit = body.match(re);
    if (!hit) return "";
    let v = hit[1].trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
      v = v.slice(1, -1);
    }
    return v.replace(/\s+/g, " ").trim();
  };
  return { name: field("name") || skillName, description: field("description") };
}

function meta(id) {
  const skill = skillById.get(id);
  if (!skill) return { name: id, description: "" };
  return frontmatter(id, skill.name);
}

function kb(bytes) {
  return bytes >= 1024 ? `${Math.round(bytes / 1024)} KB` : `${bytes} B`;
}

const categorized = new Set();
const warnings = [];
const out = [];

out.push("<!-- AUTO-GENERATED by build-index.mjs — do not edit by hand. Re-run ./update.sh. -->");
out.push("");
out.push("# Stellar/Soroban ecosystem skills — index");
out.push("");
out.push(
  `Mirror of **${manifest.skill_count} agent skills** across **${manifest.sources.length} sources** · synced ${manifest.synced_at}. ` +
    `Each skill lives under [\`skills/<source>/<skill>/\`](./skills/). Large SKILL.md files are best **grepped** for the section you need.`,
);
out.push("");
if (manifest.status && manifest.status !== "complete") {
  const missing = (manifest.missing_sources || []).join(", ") || "unknown";
  out.push(
    `> ⚠️ **PARTIAL MIRROR** (status: \`${manifest.status}\`). Missing source(s): \`${missing}\`. ` +
      `Re-run \`./update.sh\` with the required credentials for a complete mirror.`,
  );
  out.push("");
}

// Per-source pin table.
out.push("## Sources (pinned)");
out.push("");
out.push("| Source | Origin | Pinned | Skills |");
out.push("| --- | --- | --- | --- |");
for (const src of manifest.sources) {
  let origin, pin;
  if (src.type === "github") {
    origin = `[\`${src.owner}/${src.repo}\`](https://github.com/${src.owner}/${src.repo})${src.path ? ` \`${src.path}/\`` : " (root)"}`;
    pin = src.url ? `[\`${String(src.commit).slice(0, 12)}\`](${src.url})` : `\`${String(src.commit).slice(0, 12)}\``;
  } else {
    // lumenloop-archive: private repo, not git-commit-reproducible. Show the API
    // ref + a short archive sha256 so the run is at least digest-verifiable.
    origin = `\`${src.repo || src.endpoint}\` (${src.set} archive, private)`;
    const ref = src.commit_ref || src.commit || "?";
    const dg = src.archive_sha256 ? ` · zip \`sha256:${String(src.archive_sha256).slice(0, 12)}…\`` : "";
    pin = `api \`${ref}\`${dg}`;
  }
  out.push(`| \`${src.id}\` | ${origin} | ${pin} | ${src.skills.length} |`);
}
out.push("");
out.push(
  "_Every source is public GitHub, pinned to a full commit SHA (independently verifiable); " +
    "each source's upstream LICENSE/NOTICE files are vendored alongside its skills " +
    "(see `THIRD-PARTY-NOTICES.md` at the repo root)._",
);
out.push("");

// Themed groups.
for (const g of groups) {
  out.push(`## ${g.title}`);
  out.push("");
  if (g.description) {
    out.push(`_${g.description}_`);
    out.push("");
  }
  out.push("| Skill | Source | Size | What it does |");
  out.push("| --- | --- | --- | --- |");
  for (const id of g.members) {
    if (!skillById.has(id)) {
      warnings.push(`groups.json lists "${id}" but it is not in the manifest (renamed/removed upstream?)`);
      continue;
    }
    categorized.add(id);
    const skill = skillById.get(id);
    const { description } = meta(id);
    out.push(`| [\`${skill.name}\`](./skills/${id}/) | \`${skill.source}\` | ${kb(skill.totalSize)} | ${description} |`);
  }
  out.push("");
}

// Uncategorized synced skills.
const uncategorized = [...skillById.keys()].filter((id) => !categorized.has(id));
if (uncategorized.length) {
  out.push("## Uncategorized (newly synced — file these into `groups.json`)");
  out.push("");
  out.push("| Skill | Source | Size | What it does |");
  out.push("| --- | --- | --- | --- |");
  for (const id of uncategorized) {
    const skill = skillById.get(id);
    const { description } = meta(id);
    out.push(`| [\`${skill.name}\`](./skills/${id}/) | \`${skill.source}\` | ${kb(skill.totalSize)} | ${description} |`);
  }
  out.push("");
}

// Ecosystem directory snapshot (the broader map, incl. non-skill-md entries).
if (manifest.catalog && Array.isArray(manifest.catalog.entries)) {
  const cat = manifest.catalog;
  out.push("## Ecosystem directory (stellarlight.xyz catalog snapshot)");
  out.push("");
  out.push(
    `_The broader map of what exists across the Stellar agent-skill ecosystem — ${cat.entries.length} entries from ` +
      `[\`${cat.source}\`](${cat.source}), fetched ${cat.fetched_at}. Only \`skill-md\` entries are downloadable SKILL.md skills; ` +
      `\`mcp-server\` / \`sdk\` / \`cli\` / \`tool\` entries are pointers to runtime tools, not skills. Not all are mirrored here._`,
  );
  out.push("");
  out.push("| Entry | Source | Kind |");
  out.push("| --- | --- | --- |");
  for (const e of cat.entries) {
    out.push(`| \`${e.name}\` | \`${e.source}\` | \`${e.kind}\` |`);
  }
  out.push("");
}

writeFileSync(join(DIR, "INDEX.md"), out.join("\n"));

console.log(`INDEX.md written: ${categorized.size} categorized, ${uncategorized.length} uncategorized.`);
if (uncategorized.length) console.log("  uncategorized → " + uncategorized.join(", "));
for (const w of warnings) console.warn("  warning: " + w);
